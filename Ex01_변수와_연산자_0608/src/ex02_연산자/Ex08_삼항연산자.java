package ex02_연산자;

public class Ex08_삼항연산자 {

	public static void main(String[] args) {
		// 삼항연산자 - 간단한 제어처리
		// 조건식 ? true일 때 실행 할 문장 : false일 때 실행할 문장
		// 조건식 - 식의 결과가 true/false로 나옴
		
		int num1 = 1000;
		int num2 = 100;
		
		String result = num1 > num2 ? "num1이 더 큽니다" : "num2가 더 큽니다";   // "" 문자열이므로
		System.out.println(result);
		
		
		
		
	}

}
